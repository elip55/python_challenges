
#  write code that sings '100 bottles of beer on the wall'


bottles_of_beer = 99

while bottles_of_beer != 0:
    print(f'{bottles_of_beer} bottles of beer on the wall!')
    print(f'{bottles_of_beer} bottles of beeeer!')
    print(f'Take one down,\nPass it around')
    bottles_of_beer -= 1
    print(f'{bottles_of_beer} bottles of beer on the wall!\n')